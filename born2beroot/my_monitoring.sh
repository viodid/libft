ram_usage=$(free -m | awk '/Mem:/ { print $3}')
ram_total=$(free -m | awk '/Mem:/ { print $2}')
percentage=$(free -m | awk '/Mem:/ { print $3/$2 * 100.0}')
disk_usage=$(df -h | grep /$  | awk '{ print $3}')
disk_total=$(df -h | grep /$  | awk '{ print $2}')
disk_percentage=$(df -h | grep /$  | awk '{ print $5}')
wall "
  #Operating System: $(hostnamectl status | grep -i operating | cut -d ':' -f 2 | tr -d ' ')
  #Architecture: $(uname -a)
  #CPU physical: $(grep "^processor" /proc/cpuinfo | wc -l)
  #vCPU: $(grep "^processor" /proc/cpuinfo | wc -l)
  #Memory Usage: ${ram_usage}MiB/${ram_total}MiB (${percentage}%)
  #Disk Usage: ${disk_usage}/${disk_total} (${disk_percentage})
  #CPU load: $(top -bn1 | grep load | awk '{printf "%.2f%%\t\t", $(NF-2)}')
  #Last boot: $(who -b | awk '{print $3,$4}')
  #LVM use: $(if [[ $(lsblk | grep lvm | wc -l) > 0 ]]; then echo yes; else echo no; fi)
  #Connexions TCP: $(ss -s | awk '/TCP:/ {print $2}')
  #User log: $(users | wc -w)
  #Network: IP $(ip a | awk '/inet 10/ {print $2}') ($(ip a | awk '/link\/ether/ {print $2}' ))
  #Sudo: $(grep -c sudo /var/log/auth.log)
"
